/**
 * Walk all nodes and return the dynamic-attribute map portion of the reload
 * map. Each entry's `computeAttributes` call already respects
 * `DynamicAttributes.__editorConfig`, so nodes with
 * `DYNAMIC_ATTRIBUTES_EDITOR_CONFIG_HIDDEN` set will return empty classNames /
 * customAttributes — the caller can apply the result to any document to clear
 * those effects.
 */
export const buildDynamicAttributeMap = (
  editor: LexicalEditor
): types.DynamicAttributeMap => {
  return buildReloadMap(editor).dynamicAttributes;
};

/**
 * Apply a pre-built dynamic-attribute map to a specific document.
 *
 * Follows the same remove-all-possible-then-add-matched pattern as
 * `applyReloadMapToDOM`, but accepts an explicit `Document` so callers can
 * target a preview iframe's `contentDocument` instead of the global
 * `document`.
 *
 * Safe to call with `null`/`undefined` — returns immediately when no document
 * is provided.
 */
export const applyDynamicAttributesToDocument = (
  doc: Document | null | undefined,
  dynamicAttributes: types.DynamicAttributeMap
): void => {
  if (!doc) {
    return;
  }

  for (const [cssClassName, entry] of Object.entries(dynamicAttributes)) {
    const element = doc.querySelector<HTMLElement>(`.${cssClassName}`);

    if (!element) {
      continue;
    }

    const { attributes } = entry;

    // Display
    element.style.removeProperty("display");
    if (!attributes.display) {
      element.style.setProperty("display", "none");
    }

    // Class names — remove all that could have been set by any rule, then
    // add back only those from rules that currently match.
    for (const className of attributes.allPossibleClassNames) {
      element.classList.remove(className);
    }

    for (const className of attributes.classNames) {
      element.classList.add(className);
    }

    // Custom attributes — same remove-all-then-set-matched pattern.
    for (const key of attributes.allPossibleCustomAttributeKeys) {
      element.removeAttribute(key);
    }

    for (const [key, value] of Object.entries(attributes.customAttributes)) {
      element.setAttribute(key, value);
    }
  }
};

/**
 * Re-evaluate dynamic attributes, template text nodes, and optionally a named
 * collection in the editor tree, then patch the live DOM to reflect current
 * data values.
 *
 * This is the client-side counterpart to the server-side SSR/preview pipeline.
 * Call it after any client-side data mutation (pagination, ReactDecorators, etc.)
 * to keep DOM elements in sync without a full page reload.
 *
 * After patching the DOM, fires RELOAD_DYNAMIC_VALUE_COMMAND via wpHooks (when
 * provided) so subscribers can react to any data-driven DOM update without
 * being coupled to a specific collection or pagination flow.
 *
 * @param editor - The root Lexical editor instance
 * @param options.dataFetchingName - Optional name of the data fetching node that
 *   was mutated. When provided, only dynamic attribute and template text nodes
 *   that reference this data source are updated, reducing unnecessary DOM work.
 *   When omitted, all such nodes are re-evaluated.
 * @param options.collectionName - Optional name of a CollectionNode whose full
 *   inner HTML should be regenerated. When omitted, no collection HTML is
 *   regenerated.
 * @param context.wpHooks - When provided, RELOAD_DYNAMIC_VALUE_COMMAND is fired
 *   after the DOM is patched.
 * @param context.query - Query that triggered the data fetch (defaults to {}).
 * @param context.fetchedData - The raw fetched data: [data, pagination] when
 *   pagination applies, [data] otherwise.
 */
export const reloadDynamicValues = (
  editor: LexicalEditor,
  options?: {
    dataFetchingName?: string;
    collectionName?: string;
  },
  context?: {
    wpHooks?: WPHooks;
    query?: Record<string, any>;
    fetchedData?: [any, DataFetchingPagination] | [any];
    dataFetchingId?: string;
  }
): void => {
  const reloadMap = buildReloadMap(editor, options);
  applyReloadMapToDOM(reloadMap);

  if (!context?.wpHooks) {
    return;
  }

  const payload: types.DynamicValuesReloadedPayload = {
    dataFetchingName: options?.dataFetchingName,
    targetCollection: options?.collectionName,
    dataFetchingId: context.dataFetchingId,
    query: context.query ?? {},
    fetchedData: context.fetchedData,
    nodes: buildNodesMap(reloadMap),
  };

  context.wpHooks.action.doCommand(RELOAD_DYNAMIC_VALUE_COMMAND, payload);
};
